#creating lists 
#string list
friends = ["John","Paul","Mick","Dylan","Jim","Sara"]
print(friends)
#accessing the index
print(friends[2])
#will take the selected element and everyhing after that 
print(friends[2:])
#can select a range for the index
print(friends[2:4])


#can change the value at specified index
friends[0] = "Toby"
print(friends)